void putpixel(struct limine_framebuffer *fb, uint32_t x, uint32_t y, uint32_t color) {
    if (x >= fb->width || y >= fb->height) return;  // Bounds check

    volatile uint32_t *pixels = fb->address;
    uint32_t stride = fb->pitch / 4;

    pixels[y * stride + x] = color;
}

void draw_char(struct limine_framebuffer *fb, uint32_t x, uint32_t y, char c, uint32_t color) {
    for (int row = 0; row < 8; row++) {
        unsigned char bits = font8x8_basic[(int)c][row];
        for (int col = 0; col < 8; col++) {
            if (bits & (1 << (7 - col))) {
                putpixel(fb, x + col, y + row, color);
            }
        }
    }
}

void draw_text(struct limine_framebuffer *fb, uint32_t x, uint32_t y, const char *text, uint32_t color) {
    uint32_t start_x = x;
    while (*text) {
        if (*text == '\n') {
            y += 10;
            x = start_x;
        } else {
            draw_char(fb, x, y, *text, color);
            x += 8;
        }
        text++;
    }
}

void draw_rect(struct limine_framebuffer *fb, uint32_t x, uint32_t y,
               uint32_t w, uint32_t h, uint32_t color) {

    for (uint32_t yy = y; yy < y + h; yy++) {
        if (yy >= fb->height) break;

        for (uint32_t xx = x; xx < x + w; xx++) {
            if (xx >= fb->width) break;

            putpixel(fb, xx, yy, color);
        }
    }
}

void fill_screen_gray(struct limine_framebuffer *fb) {
    volatile uint32_t *pixels = fb->address;
    for (uint32_t y = 0; y < fb->height; y++) {
        for (uint32_t x = 0; x < fb->width; x++) {
            pixels[y * (fb->pitch / 4) + x] = 0x202020;
        }
    }
}

void clear_screen(struct limine_framebuffer *fb, uint32_t color) {
    volatile uint32_t *pixels = fb->address;

    uint32_t pitch = fb->pitch / 4;
    uint32_t width = fb->width;
    uint32_t height = fb->height;

    for (uint32_t y = 0; y < height; y++) {
        for (uint32_t x = 0; x < width; x++) {
            pixels[y * pitch + x] = color;
        }
    }
} // clear screen
